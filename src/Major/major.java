package Major;

public class major {
	int id;
	String name;
	String img;
	String desc;
	int xid;
	
	public int getId(){
		return this.id;
	}
	public void setId(int id){
		this.id = id;
	}
	
	public String getName(){
		return this.name;
	}
	public void setName(String name){
		this.name = name;
	}
	
	public String getImg(){
		return this.img;
	}
	public void setImg(String img){
		this.img = img;
	}

	public String getDesc(){
		return this.desc;
	}
	public void setDesc(String desc){
		this.desc = desc;
	}
	
	public int getXid(){
		return this.id;
	}
	public void setXid(int xid){
		this.xid = xid;
	}
}
